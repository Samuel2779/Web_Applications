function producto(a, b){
    return a * b
}
function resta(a, b){
    return a - b
}
function suma(a,b){
    return a + b
}
function division(a, b){
    return a / b
}
function modulo(a,b) {
    return a % b
}
exports.producto = producto;
exports.suma = suma;
exports.resta = resta;
exports.division= division;
exports.modulo = modulo;